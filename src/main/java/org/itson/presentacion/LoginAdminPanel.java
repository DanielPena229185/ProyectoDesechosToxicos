    private void checkVisibleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkVisibleActionPerformed
        JPasswordField passwordField = this.campoPassword;
        JCheckBox checkBox = this.checkVisible;
        this.alternarVisibilidadPasswordFieldConCheckBox(checkBox, passwordField);
    }//GEN-LAST:event_checkVisibleActionPerformed

    //Validar campo vacío
    private boolean validarCampoTextoUsuarioVacio(String campoTextoUsuario, String defaultText) {
        return campoTextoUsuario.isEmpty() || campoTextoUsuario.equals(defaultText);
    }

    //Validar campo contraseña vacío
    private boolean validarCampoTextoContrasenaVacio(String passwordField, String defaultText) {
        return passwordField.isEmpty() || passwordField.equals(defaultText);
    }

    // Llenar campo de texto
    private JTextField llenarCampoTexto(String text, JTextField textField) {
        textField.setText(text);
        return textField;
    }

    //Llenar campo de contraseña
    private JPasswordField llenarCampoContrasena(String text, JPasswordField passwordField) {
        passwordField.setText(text);
        return passwordField;
    }

    private JPasswordField alternarVisibilidadPasswordFieldConCheckBox(JCheckBox checkBox, JPasswordField passwordField) {
        if (checkBox.isSelected()) {
            if(validarCampoTextoContrasenaVacio(passwordField.getText(), this.contrasenaDefault)){
                this.llenarCampoContrasena("", passwordField);
            }
            passwordField.setEchoChar((char) 0);
        } else {
            if(validarCampoTextoContrasenaVacio(passwordField.getText(), this.contrasenaDefault)){
                this.llenarCampoContrasena(this.contrasenaDefault, passwordField);
            }
            passwordField.setEchoChar('•');
        }
        return passwordField;
    }

